# Basic settings:
#   domain       - The hostname to SSH to.
#   deploy_to    - Path to deploy into.
#   repository   - Git repo to clone from. (needed by mina/git)
#   branch       - Branch name to deploy. (needed by mina/git)

set :domain, 'ruby.oporaua.org'
set :deploy_to, '/home/publicwhip-lutsk'
set :repository, 'git@github.com:OPORA/publicwhip-lutsk.git'
set :branch, 'master'

# For system-wide RVM install.
set :rvm_use_path,  '/usr/local/rvm/scripts/rvm'
set :keep_releases, 2
## Optional settings:
set :user, 'root'          # Username in the server to SSH to.
set :port, '1122'           # SSH port number.
#   set :forward_agent, true     # SSH forward_agent.

# shared dirs and files will be symlinked into the app-folder by the 'deploy:link_shared_paths' step.
set :shared_dirs, fetch(:shared_dirs, []).push('log', 'public/image')
set :shared_files, fetch(:shared_files, []).push('config/database.yml', 'config/secrets.yml')
invoke :'rvm:use', 'ruby-2.4.1@default'
